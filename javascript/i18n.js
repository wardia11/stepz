let LANG = (typeof localStorage !== "undefined" && localStorage.getItem("stepz_lang")) || "fr";

function t(key) {
  return (I18N[LANG] && I18N[LANG][key]) || I18N.fr[key] || key;
}

function tColor(c) {
  return LANG === "ar" ? (COLORS_AR[c] || c) : c;
}

function tBadge(key) {
  return (BADGES[key] && BADGES[key][LANG]) || key;
}

function pName(p) {
  return p.category === "enfant" ? (p.name + " " + t("kids_suffix")) : p.name;
}

function formatPrice(value) {
  const n = Math.round(Number(value) || 0).toLocaleString("fr-FR");
  return LANG === "ar" ? (n + " دج") : (n + " DA");
}

function setLang(lang) {
  localStorage.setItem("stepz_lang", lang);
  window.location.reload();
}

document.addEventListener("DOMContentLoaded", () => {
  document.documentElement.lang = LANG;
  document.documentElement.dir = LANG === "ar" ? "rtl" : "ltr";

  document.querySelectorAll("[data-i18n]").forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-html]").forEach(el => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });
  document.querySelectorAll("[data-i18n-ph]").forEach(el => {
    el.setAttribute("placeholder", t(el.dataset.i18nPh));
  });

  const toggle = document.getElementById("lang-toggle");
  if (toggle) {
    toggle.textContent = LANG === "fr" ? "العربية" : "Français";
    toggle.addEventListener("click", () => setLang(LANG === "fr" ? "ar" : "fr"));
  }

  const themeBtn = document.getElementById("theme-toggle");
  if (themeBtn) {
    var setThemeIcon = function () {
      var dark = document.documentElement.getAttribute("data-theme") === "dark";
      themeBtn.textContent = dark ? "☀️" : "🌙";
    };
    setThemeIcon();
    themeBtn.addEventListener("click", function () {
      var dark = document.documentElement.getAttribute("data-theme") === "dark";
      if (dark) {
        document.documentElement.removeAttribute("data-theme");
        localStorage.setItem("stepz_theme", "light");
      } else {
        document.documentElement.setAttribute("data-theme", "dark");
        localStorage.setItem("stepz_theme", "dark");
      }
      setThemeIcon();
    });
  }
});
